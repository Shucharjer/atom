template <aggregate Ty>
struct outline {
    [[maybe_unused]] constexpr static std::remove_cvref_t<Ty> value{};
};

template <typename Ty>
constexpr static const std::remove_cvref_t<Ty>& get_object_outline() noexcept {
    return outline<Ty>::value;
}

template <typename Ty>
struct tuple_view_helper;

template <typename Ty>
requires(member_count_of<Ty>() == 0)
struct tuple_view_helper<Ty> {
    constexpr static auto tuple_view() noexcept { return std::tuple<>(); }
    constexpr static auto tuple_view(const Ty& obj) noexcept {
        return std::tuple<>();
    }
};

